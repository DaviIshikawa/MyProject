# Demonstração de uso de operadores aritmédicos...
X = int(input("Digite o valor de X: "))
Y = int(input("Digite o valor de Y: "))

SOMAR = X + Y
print("O resultado da soma: ", SOMAR)

SUBTRAIR = X - Y
print("O resultado da subtração: ", SUBTRAIR)

MULTIPLICAR = X * Y 
print("O resultado da multiplicação: ", MULTIPLICAR)

DIVIDIR = X / Y
print("O resultado da divisão: ", DIVIDIR)


